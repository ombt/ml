config_filename <- "config.csv"
if ( ! file.exists(config_filename)) {
    if (nchar(Sys.getenv("DEV_ROOT")) == 0) {
        stop("No 'config.file' found")
    }
    config_filename <- file.path(Sys.getenv("DEV_ROOT"),
