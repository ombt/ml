sink("out2.log")

db_conn <- dx_connect_to_db(config)

now = Sys.Date()
test_date_format <- "%Y-%m-%d"
now_string <- format(now, test_date_format)

for (dx.tbl in dx.tbls) {
    print(sprintf("DX TBL ... %s", dx.tbl))
    query <- sprintf("select max(transaction_date) as max_day from dx.%s where transaction_date <= '%s'", dx.tbl, now_string)
    print(sprintf("DX TBL QUERY ... %s", query))
    tryCatch({
            print(dbGetQuery(db_conn, query))
        },
        error=function(e) { print(e); }
    )
}

